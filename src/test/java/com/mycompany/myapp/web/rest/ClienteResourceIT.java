    @Test
    @Transactional
    void putNewCliente() throws Exception {
        // Initialize the database
        clienteRepository.saveAndFlush(cliente);

        int databaseSizeBeforeUpdate = clienteRepository.findAll().size();

        // Update the cliente
        Cliente updatedCliente = clienteRepository.findById(cliente.getId()).get();
        // Disconnect from session so that the updates on updatedCliente are not directly saved in db
        em.detach(updatedCliente);
        updatedCliente
            .fecha(UPDATED_FECHA)
            .observaciones(UPDATED_OBSERVACIONES)
            .observacionesContentType(UPDATED_OBSERVACIONES_CONTENT_TYPE)
            .createByUser(UPDATED_CREATE_BY_USER)
            .createdOn(UPDATED_CREATED_ON)
            .modifyByUser(UPDATED_MODIFY_BY_USER)
            .modifiedOn(UPDATED_MODIFIED_ON)
            .auditStatus(UPDATED_AUDIT_STATUS);

        restClienteMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedCliente.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedCliente))
            )
            .andExpect(status().isOk());

        // Validate the Cliente in the database
        List<Cliente> clienteList = clienteRepository.findAll();
        assertThat(clienteList).hasSize(databaseSizeBeforeUpdate);
        Cliente testCliente = clienteList.get(clienteList.size() - 1);
        assertThat(testCliente.getFecha()).isEqualTo(UPDATED_FECHA);
        assertThat(testCliente.getObservaciones()).isEqualTo(UPDATED_OBSERVACIONES);
        assertThat(testCliente.getObservacionesContentType()).isEqualTo(UPDATED_OBSERVACIONES_CONTENT_TYPE);
        assertThat(testCliente.getCreateByUser()).isEqualTo(UPDATED_CREATE_BY_USER);
        assertThat(testCliente.getCreatedOn()).isEqualTo(UPDATED_CREATED_ON);
        assertThat(testCliente.getModifyByUser()).isEqualTo(UPDATED_MODIFY_BY_USER);
        assertThat(testCliente.getModifiedOn()).isEqualTo(UPDATED_MODIFIED_ON);
        assertThat(testCliente.getAuditStatus()).isEqualTo(UPDATED_AUDIT_STATUS);
    }

