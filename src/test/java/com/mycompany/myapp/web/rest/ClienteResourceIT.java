    @Test
    @Transactional
    void createCliente() throws Exception {
        int databaseSizeBeforeCreate = clienteRepository.findAll().size();
        // Create the Cliente
        restClienteMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(cliente)))
            .andExpect(status().isCreated());

        // Validate the Cliente in the database
        List<Cliente> clienteList = clienteRepository.findAll();
        assertThat(clienteList).hasSize(databaseSizeBeforeCreate + 1);
        Cliente testCliente = clienteList.get(clienteList.size() - 1);
        assertThat(testCliente.getFecha()).isEqualTo(DEFAULT_FECHA);
        assertThat(testCliente.getObservaciones()).isEqualTo(DEFAULT_OBSERVACIONES);
        assertThat(testCliente.getObservacionesContentType()).isEqualTo(DEFAULT_OBSERVACIONES_CONTENT_TYPE);
        assertThat(testCliente.getCreateByUser()).isEqualTo(DEFAULT_CREATE_BY_USER);
        assertThat(testCliente.getCreatedOn()).isEqualTo(DEFAULT_CREATED_ON);
        assertThat(testCliente.getModifyByUser()).isEqualTo(DEFAULT_MODIFY_BY_USER);
        assertThat(testCliente.getModifiedOn()).isEqualTo(DEFAULT_MODIFIED_ON);
        assertThat(testCliente.getAuditStatus()).isEqualTo(DEFAULT_AUDIT_STATUS);
    }

