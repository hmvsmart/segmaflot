import axios from 'axios';

import { IAditamentoUnidad } from '@/shared/model/aditamento-unidad.model';

const baseApiUrl = 'api/aditamento-unidads';

export default class AditamentoUnidadService {
  public find(id: number): Promise<IAditamentoUnidad> {
    return new Promise<IAditamentoUnidad>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IAditamentoUnidad): Promise<IAditamentoUnidad> {
    return new Promise<IAditamentoUnidad>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IAditamentoUnidad): Promise<IAditamentoUnidad> {
    return new Promise<IAditamentoUnidad>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IAditamentoUnidad): Promise<IAditamentoUnidad> {
    return new Promise<IAditamentoUnidad>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
